function shareLesson(day, title, shareType) {
    const message = `🕊️ I just completed Day ${day}: "${title}" in my Shmiras HaLashon journey! Join me in learning to guard our speech. #ChofetzChaim #ShmirasHaLashon`;
    const url = window.location.href;
    
    switch(shareType) {
        case 'whatsapp':
            const whatsappUrl = `https://wa.me/?text=${encodeURIComponent(message + '\n' + url)}`;
            window.open(whatsappUrl, '_blank');
            break;
            
        case 'sms':
            const smsUrl = `sms:?&body=${encodeURIComponent(message + '\n' + url)}`;
            window.location.href = smsUrl;
            break;
            
        case 'twitter':
            const twitterUrl = `https://twitter.com/intent/tweet?text=${encodeURIComponent(message)}&url=${encodeURIComponent(url)}`;
            window.open(twitterUrl, '_blank');
            break;
            
        case 'copy':
            navigator.clipboard.writeText(message + '\n' + url).then(() => {
                alert('✓ Copied to clipboard! Share it with your friends.');
            }).catch(() => {
                alert('Failed to copy. Please try again.');
            });
            break;
    }
}
